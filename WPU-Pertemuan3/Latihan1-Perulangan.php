<?php
    // Perulangan

    // for
    for($i = 0; $i < 5; $i++){
        echo "Hello World 1!<br>";
    }

    // while
    $i = 0;
    while($i < 5){
        echo "Hello World 2!<br>";
        $i++;
    }

    // do .. while
    $i = 0;
    do{
        echo "Hello World 3!<br>";
        $i++;
    } while($i < 5);
    
    // foreach (perulangan khusus array)

?>